// Get sticker image URL from LINE CDN
export const getStickerImageUrl = (stickerId: string): string => {
  return `https://stickershop.line-scdn.net/stickershop/v1/sticker/${stickerId}/android/sticker.png`;
};

// Get all stickers from all packages
export const getAllStickers = (): Array<{ packageId: string; stickerId: string; packageName: string }> => {
  const allStickers: Array<{ packageId: string; stickerId: string; packageName: string }> = [];
  
  for (const pkg of LINE_STICKER_PACKAGES) {
    for (const stickerId of pkg.stickers) {
      allStickers.push({
        packageId: pkg.packageId,
        stickerId,
        packageName: pkg.name
      });
    }
  }
  
  return allStickers;
};
